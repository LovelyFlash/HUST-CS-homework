/*
任务描述
求Π的近似值。输入精度e，根据计算公式：Π/4=1-1/3+1/5-1/7+...，直到最后一项的绝对值小于e为止（该项需要加上去）。结果保留6位小数。

相关知识
循环控制；
奇偶判断。

编程要求
根据提示，在右侧Begin-End区域编写一个完整满足任务要求的程序。

测试说明
平台会对你编写的代码进行测试，若是与预期输出相同，则算通关。

输入精度：
0.0000001
预期输出：
3.141593

开始你的任务吧，祝你成功！
*/
#include <stdio.h>

int main() {
    double e, pi = 0.0, term;
    int k = 1, sign = 1;

    // 输入精度e
    scanf("%lf", &e);

    // 计算直到最后一项的绝对值小于e
    do {
        term = sign * 1.0 / k;
        pi += term;
        k += 2;
        sign = -sign; // 每次符号翻转
    } while (term >= e || term <= -e);

    // 乘以4得到pi的近似值
    pi *= 4;

    // 输出保留6位小数的结果
    printf("%.6f\n", pi);

    return 0;
}
